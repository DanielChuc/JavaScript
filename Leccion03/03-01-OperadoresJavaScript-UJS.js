//Operadores
//Suma
let a = 3, b = 2;
let z = a + b;
console.log("El resutado de la suma: " + z);
//Resta
z = a - b;
console.log("El resutado de la resta: " + z);
//Multiplicacion
z = a * b;
console.log("El resutado de la multiplicacion: " + z);
//Division
z = a / b;
console.log("El resutado de la division: " + z);
//Residuo o modulo
z = a % b;
console.log("El resutado de la operacion modulo (residuo): " + z);
//Exponente
z = a ** b;
console.log("El resutado de operador exponente: " + z);