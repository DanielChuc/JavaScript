/* Ciclo for tabla de multiplicar */
var valor = 2;
for (let i = 0; i <= 12; i++) {
    console.log(valor + " x " + i + ' = ' + valor * i);


}
/* Ciclo while tabla de multiplicar */
var valor = 2;
var e = 0;
while (e < 12) {
    e += 1;
    console.log(valor + " x " + e + ' = ' + valor * e);

}


/* Ciclo dowhile tabla de multiplicar */
var valor = 2;
var e = 0;
do {
    e += 1;
    console.log(valor + " x " + e + ' = ' + valor * e);

} while (e < 12);