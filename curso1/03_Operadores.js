/**
 * Operadores de asignación
 */
//Operador de asignación
var x = 2;
var y = true;

//Operador de asignación de adición +=
var x = 2;
var y = 1;
x += y;
console.log(x);

//Operador de asignación de resta -=
var x = 1;
var y = 2;
x -= y;
console.log(x);

//Operador de asignación de multiplicación *=

var x = 1;
var y = 2;
x *= y;
console.log(x);


//Operador de asignación de División /=

var x = 1;
var y = 2;
x /= y;
console.log(x);

//Operador de asignación de residuo %=

var x = 1;
var y = 2;
x %= y;
console.log(x);

//Operador de asignación de exponencial **=

var x = 2;
var y = 3;
x **= y; //(2³)
console.log(x);