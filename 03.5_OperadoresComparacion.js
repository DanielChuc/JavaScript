//Operador igual ==

console.log(3 == 3);
console.log(3 == '3');

//Operador estrictamente igual ===
console.log(3 === '3');

//Operador no es igual !=
console.log(3 != 3);
console.log(3 != '3');

//Operador de desigualdad estricta !==
console.log(3 !== '3');

//Operadores >, >=, <, <=
console.log(3 > 4);
console.log(3 >= 4);
console.log(3 < 4);
console.log(3 <= 4);

/**
 * Operadores aritmetricos
 * +, -, *, /, %, **
 */
console.log(2 + 2);
console.log(2 - 2);
console.log(2 * 2);
console.log(2 / 2);
console.log(2 % 2);
console.log(2 ** 2);

